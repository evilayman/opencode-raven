  function estimateAvoidedMcpSchemaBytes(tools: any[]): number {
    if (!tools.length) return 0
    return JSON.stringify(tools).length
  }

  function synthesizeMcpDescription(serverName: string, tools: OnDemandMcpToolInfo[]): string {
    if (!tools.length) return `${serverName} exposes on-demand MCP capabilities. Use list_tools for available operations.`
    const descriptions = tools.map((item) => item.description || item.name).filter(Boolean)
    const joined = descriptions.slice(0, 6).join("; ")
    return compactText(`${serverName} provides on-demand MCP tools for ${joined}.`)
  }

  async function refreshOnDemandMcpDescriptions(force = false, serverName?: string, signal?: AbortSignal): Promise<{ refreshed: string[]; failed: string[] }> {
    if (signal?.aborted) return { refreshed: [], failed: [] }
    const entries = activeOnDemandMcpEntries()
      .filter(([name]) => !serverName || name.toLowerCase() === serverName.toLowerCase())
      .filter(([name]) => {
        const metadata = mcpMetadata[name]
        return force || metadata?.status === "pending" || metadata?.status === "failed" || !metadata?.autoGeneratedDescription || !metadata.autoGeneratedTools?.length || metadata.avoidedSchemaBytes === undefined
      })
    const refreshed: string[] = []
    const failed: string[] = []
    if (!entries.length) return { refreshed, failed }
    let changed = false
    for (const [name, server] of entries) {
      if (signal?.aborted) break
      let connection: McpConnection | undefined
      const checkedAt = new Date().toISOString()
      try {
        connection = await connectOnDemandMcp(name, server, signal)
        const rawTools = await listAllMcpTools(connection.client, { signal, timeout: mcpTimeoutMs(server) })
        const tools = generatedToolsFromMcpTools(rawTools)
        mcpMetadata[name] = {
          autoGeneratedTools: tools,
          autoGeneratedDescription: synthesizeMcpDescription(name, tools),
          avoidedSchemaBytes: estimateAvoidedMcpSchemaBytes(rawTools),
          status: "ready",
          lastCheckedAt: checkedAt,
        }
        changed = true
        refreshed.push(name)
      } catch (err: any) {
        if (signal?.aborted) break
        mcpMetadata[name] = {
          ...(mcpMetadata[name] ?? {}),
          status: "failed",
          lastError: truncateText(err?.message ?? err, 500),
          lastCheckedAt: checkedAt,
        }
        changed = true
        failed.push(name)
        // Keep startup/discovery best-effort; /raven mcp exposes failures.
      } finally {
        if (connection) await closeMcpConnection(connection)
      }
    }
    if (changed) {
      saveMcpMetadata(mcpMetadata)
      saveDynamicGuidance()
    }
    return { refreshed, failed }
  }

  function waitForRefreshDelay(ms: number, signal: AbortSignal): Promise<void> {
    if (signal.aborted) return Promise.resolve()
    return new Promise((resolve) => {
      const timer = setTimeout(done, ms)
      function done() {
        clearTimeout(timer)
        signal.removeEventListener("abort", done)
        resolve()
      }
      signal.addEventListener("abort", done, { once: true })
    })
  }

  function queueOnDemandMcpRefresh(force = false, serverName?: string, delay = 0): Promise<{ refreshed: string[]; failed: string[] }> {
    const controller = new AbortController()
    mcpRefreshControllers.add(controller)
    const run = mcpRefreshQueue
      .catch(() => undefined)
      .then(async () => {
        if (delay) await waitForRefreshDelay(delay, controller.signal)
        return refreshOnDemandMcpDescriptions(force, serverName, controller.signal)
      })
      .finally(() => mcpRefreshControllers.delete(controller))
    mcpRefreshQueue = run
    return run
  }

  function scheduleOnDemandMcpRefresh(): Promise<{ refreshed: string[]; failed: string[] }> {
    if (mcpRefreshScheduled) return mcpRefreshPromise!
    mcpRefreshScheduled = true
    mcpRefreshPromise = queueOnDemandMcpRefresh(false, undefined, 1000)
    return mcpRefreshPromise
  }

  function onDemandMcpRuntimeStatus(name: string): "pending" | "ready" | "failed" {
    const metadata = mcpMetadata[name]
    if (metadata?.status) return metadata.status
    return "pending"
  }

  function displayMcpStatus(status: "pending" | "ready" | "failed"): "pending" | "loaded" | "failed" {
    return status === "ready" ? "loaded" : status
  }

  function onDemandMcpStatusBuckets(): { loaded: string[]; failed: string[]; pending: string[] } {
    const buckets = { loaded: [] as string[], failed: [] as string[], pending: [] as string[] }
    for (const [name] of activeOnDemandMcpEntries()) {
      const status = onDemandMcpRuntimeStatus(name)
      if (status === "ready") buckets.loaded.push(name)
      else if (status === "failed") buckets.failed.push(name)
      else buckets.pending.push(name)
    }
    return buckets
  }

  function failedOnDemandMcpNames(names?: string[]): string[] {
    const configured = activeOnDemandMcpEntries().map(([name]) => name)
    const wanted = names?.length ? names : configured
    return wanted.filter((name) => configured.includes(name) && onDemandMcpRuntimeStatus(name) === "failed")
  }

  function setOnDemandMcpHealth(serverName: string, status: "ready" | "failed", error?: unknown) {
    const match = getOnDemandMcpServer(serverName)
    if (!match) return undefined
    const [name] = match
    const next: OnDemandMcpMetadata = {
      ...(mcpMetadata[name] ?? {}),
      status,
      lastCheckedAt: new Date().toISOString(),
    }
    if (status === "failed") next.lastError = truncateText(error, 500)
    else delete next.lastError
    mcpMetadata[name] = next
    saveMcpMetadata(mcpMetadata)
    saveDynamicGuidance()
    return name
  }

  function onDemandMcpStatus(): string {
    const entries = activeOnDemandMcpEntries()
    if (!Object.keys(config.raven_mcp.onDemandMcpServers ?? {}).length) return "Raven on-demand MCPs: none configured."
    if (!entries.length) return "Raven on-demand MCPs: none enabled."
    const buckets = onDemandMcpStatusBuckets()
    const lines = entries.map(([name, server]) => {
      const metadata = mcpMetadata[name]
      const source = server.description ? "user description" : "no user description"
      const generated = metadata?.autoGeneratedDescription ? "generated" : "missing generated description"
      const tools = metadata?.autoGeneratedTools?.length ? `${metadata.autoGeneratedTools.length} tools` : "tools pending"
      const status = onDemandMcpRuntimeStatus(name)
      const avoided = status === "ready" && metadata?.avoidedSchemaBytes ? `, ${formatBytes(metadata.avoidedSchemaBytes)} avoided schema` : ""
      const error = status === "failed" && metadata?.lastError ? `, error: ${metadata.lastError}` : ""
      return `- ${name} (${server.type}): ${displayMcpStatus(status)}, ${source}, ${generated}, ${tools}${avoided}${error}`
    })
    return `Raven on-demand MCPs (${config.raven_mcp.descriptionDetail ?? "full"} guidance):\n  Loaded: ${buckets.loaded.length ? buckets.loaded.join(", ") : "none"}\n  Failed: ${buckets.failed.length ? buckets.failed.join(", ") : "none"}\n  Pending: ${buckets.pending.length ? buckets.pending.join(", ") : "none"}\n${lines.join("\n")}`
  }

  function avoidedMcpSchemaBytes(): number {
    return activeOnDemandMcpEntries().reduce((sum, [name]) => {
      const metadata = mcpMetadata[name]
      if (onDemandMcpRuntimeStatus(name) !== "ready") return sum
      return sum + Math.max(0, metadata?.avoidedSchemaBytes ?? 0)
    }, 0)
  }

