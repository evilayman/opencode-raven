function normalizeToolInfo(value: unknown): OnDemandMcpToolInfo[] | undefined {
  if (!Array.isArray(value)) return undefined
  const tools = value
    .filter((item) => item && typeof item === "object" && typeof item.name === "string" && item.name.trim())
    .map((item) => ({
      name: item.name.trim(),
      ...(typeof item.description === "string" && item.description.trim() ? { description: item.description.trim() } : {}),
    }))
  return tools.length ? tools : undefined
}

function normalizeOnDemandMcpServer(value: unknown): OnDemandMcpServer | undefined {
  if (!value || typeof value !== "object") return undefined
  const source = value as Record<string, any>
  const base = {
    ...(typeof source.description === "string" && source.description.trim() ? { description: source.description.trim() } : {}),
  }

  if (source.type === "stdio") {
    if (typeof source.command !== "string" || !source.command.trim()) return undefined
    return {
      ...base,
      type: "stdio",
      command: source.command.trim(),
      ...(Array.isArray(source.args) ? { args: source.args.filter((arg: unknown) => typeof arg === "string") } : {}),
      ...(source.env && typeof source.env === "object" ? { env: Object.fromEntries(Object.entries(source.env).filter((entry): entry is [string, string] => typeof entry[1] === "string")) } : {}),
      ...(typeof source.cwd === "string" && source.cwd.trim() ? { cwd: source.cwd.trim() } : {}),
    }
  }

  if (source.type === "remote" || typeof source.url === "string") {
    if (typeof source.url !== "string" || !source.url.trim()) return undefined
    return {
      ...base,
      type: "remote",
      url: source.url.trim(),
      ...(source.headers && typeof source.headers === "object" ? { headers: Object.fromEntries(Object.entries(source.headers).filter((entry): entry is [string, string] => typeof entry[1] === "string")) } : {}),
    }
  }

  return undefined
}

function normalizeOnDemandMcpMetadata(value: unknown, fallback: Record<string, OnDemandMcpMetadata> = {}): Record<string, OnDemandMcpMetadata> {
  const source = value && typeof value === "object" ? value as Record<string, any> : fallback
  const result: Record<string, OnDemandMcpMetadata> = {}
  for (const [key, raw] of Object.entries(source)) {
    if (!key.trim() || !raw || typeof raw !== "object") continue
    const item = raw as Record<string, unknown>
    const metadata: OnDemandMcpMetadata = {}
    if (typeof item.autoGeneratedDescription === "string" && item.autoGeneratedDescription.trim()) {
      metadata.autoGeneratedDescription = item.autoGeneratedDescription.trim()
    }
    const tools = normalizeToolInfo(item.autoGeneratedTools)
    if (tools) metadata.autoGeneratedTools = tools
    if (typeof item.avoidedSchemaBytes === "number" && Number.isFinite(item.avoidedSchemaBytes) && item.avoidedSchemaBytes >= 0) {
      metadata.avoidedSchemaBytes = Math.round(item.avoidedSchemaBytes)
    }
    if (metadata.autoGeneratedDescription || metadata.autoGeneratedTools?.length || metadata.avoidedSchemaBytes) result[key.trim()] = metadata
  }
  return result
}

function extractOnDemandMcpMetadata(value: unknown): Record<string, OnDemandMcpMetadata> {
  return normalizeOnDemandMcpMetadata(value)
}

function normalizeOnDemandMcpServers(value: unknown, fallback: Record<string, OnDemandMcpServer> = {}): Record<string, OnDemandMcpServer> {
  const source = value && typeof value === "object" ? value as Record<string, unknown> : fallback
  const result: Record<string, OnDemandMcpServer> = {}
  for (const [key, raw] of Object.entries(source)) {
    if (!key.trim()) continue
    const server = normalizeOnDemandMcpServer(raw)
    if (server) result[key.trim()] = server
  }
  return result
}

