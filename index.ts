  function truncateText(value: unknown, max = 240): string {
    const text = String(value ?? "").replace(/\s+/g, " ").trim()
    return text.length > max ? `${text.slice(0, Math.max(0, max - 3))}...` : text
  }

  function onDemandMcpGuidance(target: "delegate" | "raven" = "delegate"): string {
    const servers = config.onDemandMcpServers ?? {}
    const entries = Object.entries(servers)
    if (!entries.length) return "No on-demand MCPs are configured."

    const detail = config.onDemandMcpDescriptionDetail ?? "full"
    const lines = entries.map(([name, server]) => {
      const metadata = mcpMetadata[name] ?? {}
      const userDescription = truncateText(server.description, 220)
      const generated = truncateText(metadata.autoGeneratedDescription, 260)
      const parts = [userDescription, generated ? `Discovered capabilities: ${generated}` : ""].filter(Boolean)
      const summary = parts.length ? parts.join(" ") : "Description discovery pending."
      if (detail === "minimized" || !metadata.autoGeneratedTools?.length) return `- ${name}: ${summary}`
      const tools = metadata.autoGeneratedTools
        .map((item) => `${item.name}${item.description ? ` - ${truncateText(item.description, 140)}` : ""}`)
        .join("; ")
      return `- ${name}: ${summary} Tools: ${tools}`
    })
    if (target === "raven") {
      return `Raven can handle these on-demand MCP capabilities through raven_mcp:\n${lines.join("\n")}`
    }
    return `Raven can handle these on-demand MCP capabilities. Delegate matching requests with raven_seek; include the target MCP server and likely tool name when obvious, but do not call raven_mcp directly:\n${lines.join("\n")}`
  }

  function resolveEnvPlaceholders(value: string): string {
    return value.replace(/\{env:([A-Za-z_][A-Za-z0-9_]*)\}/g, (_match, name) => {
      const envValue = process.env[name]
      if (envValue === undefined) throw new Error(`Missing environment variable ${name}`)
      return envValue
    })
  }

  function resolveStringRecord(value: Record<string, string> | undefined): Record<string, string> | undefined {
    if (!value) return undefined
    const result: Record<string, string> = {}
    for (const [key, raw] of Object.entries(value)) {
      result[key] = resolveEnvPlaceholders(raw)
    }
    return result
  }

  function cleanProcessEnv(): Record<string, string> {
    const result: Record<string, string> = {}
    for (const [key, value] of Object.entries(process.env)) {
      if (typeof value === "string") result[key] = value
    }
    return result
  }

  async function withTimeout<T>(promise: Promise<T>, ms: number, label: string): Promise<T> {
    let timer: ReturnType<typeof setTimeout> | undefined
    try {
      return await Promise.race([
        promise,
        new Promise<never>((_, reject) => {
          timer = setTimeout(() => reject(new Error(`${label} timed out after ${Math.round(ms / 1000)}s`)), ms)
        }),
      ])
    } finally {
      if (timer) clearTimeout(timer)
    }
  }

  type McpConnection = {
    client: Client
    transport: any
    serverName: string
    lastUsed: number
  }

  const mcpConnections = new Map<string, McpConnection>()

  async function closeMcpConnection(connection: McpConnection) {
    try {
      if (typeof connection.transport?.terminateSession === "function") {
        await connection.transport.terminateSession()
      }
    } catch { /* best-effort */ }
    try {
      await connection.client.close()
    } catch { /* best-effort */ }
  }

  async function closeMcpConnectionKey(key: string) {
    const existing = mcpConnections.get(key)
    if (!existing) return
    mcpConnections.delete(key)
    await closeMcpConnection(existing)
  }

  function closeIdleMcpConnections(maxAgeMs = 10 * 60 * 1000) {
    const now = Date.now()
    for (const [key, connection] of mcpConnections) {
      if (now - connection.lastUsed > maxAgeMs) {
        void closeMcpConnectionKey(key)
      }
    }
  }

  function mcpConnectionKey(sessionId: string, serverName: string): string {
    return `${sessionId}:${serverName.toLowerCase()}`
  }

  function getOnDemandMcpServer(serverName: string): [string, OnDemandMcpServer] | undefined {
    const entries = Object.entries(config.onDemandMcpServers ?? {})
    return entries.find(([name]) => name.toLowerCase() === serverName.toLowerCase())
  }

  async function connectOnDemandMcp(serverName: string, server: OnDemandMcpServer): Promise<McpConnection> {
    const timeout = (config.timeout ?? 180) * 1000
    const client = new Client({ name: PACKAGE_NAME, version: PACKAGE_VERSION })

    if (server.type === "stdio") {
      const env = { ...cleanProcessEnv(), ...resolveStringRecord(server.env) }
      const transport = new StdioClientTransport({
        command: resolveEnvPlaceholders(server.command),
        args: server.args?.map((arg) => resolveEnvPlaceholders(arg)),
        env,
        cwd: server.cwd ? resolveEnvPlaceholders(server.cwd) : undefined,
      })
      await withTimeout(client.connect(transport), timeout, `Connecting to ${serverName}`)
      return { client, transport, serverName, lastUsed: Date.now() }
    }

    const url = new URL(resolveEnvPlaceholders(server.url))
    const headers = resolveStringRecord(server.headers)
    try {
      const transport = new StreamableHTTPClientTransport(url, headers ? { requestInit: { headers } } : undefined)
      await withTimeout(client.connect(transport), timeout, `Connecting to ${serverName}`)
      return { client, transport, serverName, lastUsed: Date.now() }
    } catch (err) {
      try { await client.close() } catch { /* best-effort */ }
      const fallbackClient = new Client({ name: PACKAGE_NAME, version: PACKAGE_VERSION })
      const fallback = new SSEClientTransport(url, headers ? { requestInit: { headers } } : undefined)
      await withTimeout(fallbackClient.connect(fallback), timeout, `Connecting to ${serverName} via SSE`)
      return { client: fallbackClient, transport: fallback, serverName, lastUsed: Date.now() }
    }
  }

  async function getMcpConnection(sessionId: string, serverName: string): Promise<McpConnection> {
    closeIdleMcpConnections()
    const match = getOnDemandMcpServer(serverName)
    if (!match) throw new Error(`Unknown on-demand MCP server: ${serverName}`)
    const [canonicalName, server] = match
    const key = mcpConnectionKey(sessionId, canonicalName)
    const existing = mcpConnections.get(key)
    if (existing) {
      existing.lastUsed = Date.now()
      return existing
    }
    const connection = await connectOnDemandMcp(canonicalName, server)
    mcpConnections.set(key, connection)
    return connection
  }

