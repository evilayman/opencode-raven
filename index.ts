  function orderedOnDemandMcpEntries(entries: [string, OnDemandMcpServer][], preferredOrder?: string[]): [string, OnDemandMcpServer][] {
    if (!preferredOrder?.length) return entries
    const order = new Map(preferredOrder.map((name, index) => [name.toLowerCase(), index]))
    const existing = entries
      .filter(([name]) => order.has(name.toLowerCase()))
      .sort(([left], [right]) => order.get(left.toLowerCase())! - order.get(right.toLowerCase())!)
    const added = entries.filter(([name]) => !order.has(name.toLowerCase()))
    return [...existing, ...added]
  }

  function firstSentenceText(value: unknown): string {
    const text = compactText(value)
    const period = text.indexOf(".")
    return period === -1 ? text : text.slice(0, period + 1)
  }

  function onDemandMcpGuidance(target: "delegate" | "raven" = "delegate", preferredOrder?: string[]): string {
    const servers = config.raven_mcp.onDemandMcpServers ?? {}
    const activeEntries = activeOnDemandMcpEntries()
    const entries = orderedOnDemandMcpEntries(
      target === "raven"
        ? activeEntries
        : activeEntries.filter(([name]) => onDemandMcpRuntimeStatus(name) === "ready"),
      preferredOrder,
    )
    if (!Object.keys(servers).length) return "No on-demand MCPs are configured."
    if (!activeEntries.length) return "No enabled on-demand MCPs are configured."
    if (!entries.length) return "No loaded on-demand MCPs are available."

    const detail = config.raven_mcp.descriptionDetail ?? "full"
    const lines = entries.map(([name, server]) => {
      const metadata = mcpMetadata[name] ?? {}
      const userDescription = firstSentenceText(server.description)
      const generated = firstSentenceText(metadata.autoGeneratedDescription)
      const parts = [userDescription, generated ? `Discovered capabilities: ${generated}` : ""].filter(Boolean)
      const summary = parts.length ? parts.join(" ") : "Description discovery pending."
      if (detail === "minimized" || !metadata.autoGeneratedTools?.length) return `- ${name}: ${summary}`
      const tools = metadata.autoGeneratedTools
        .map((item) => `${item.name}${item.description ? ` - ${firstSentenceText(item.description)}` : ""}`)
        .join("; ")
      return `- ${name}: ${summary} Tools: ${tools}`
    })
    if (target === "raven") {
      return `Use raven_mcp_bridge for these on-demand MCP capabilities:\n${lines.join("\n")}`
    }
    return `Non-Raven agents can use these on-demand MCP capabilities through the public raven_mcp delegator. Use raven_mcp for matching requests and include the target MCP server and likely capability when obvious. The raven-mcp agent itself must call raven_mcp_bridge directly instead:\n${lines.join("\n")}`
  }

  function resolveEnvPlaceholders(value: string): string {
    return value.replace(/\{env:([A-Za-z_][A-Za-z0-9_]*)\}/g, (_match, name) => {
      const envValue = process.env[name]
      if (envValue === undefined) throw new Error(`Missing environment variable ${name}`)
      return envValue
    })
  }

  function resolveStringRecord(value: Record<string, string> | undefined): Record<string, string> | undefined {
    if (!value) return undefined
    const result: Record<string, string> = {}
    for (const [key, raw] of Object.entries(value)) {
      result[key] = resolveEnvPlaceholders(raw)
    }
    return result
  }

  function cleanProcessEnv(): Record<string, string> {
    const result: Record<string, string> = {}
    for (const [key, value] of Object.entries(process.env)) {
      if (typeof value === "string") result[key] = value
    }
    return result
  }

