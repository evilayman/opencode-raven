  function formatMcpList(title: string, items: any[], fields: string[] = ["name", "description"]): string {
    if (!items.length) return `${title}: none found.`
    const lines = items.map((item) => {
      const primary = fields.map((field) => item?.[field]).find(Boolean) ?? JSON.stringify(item)
      const description = item?.description && primary !== item.description ? ` - ${truncateText(item.description, 300)}` : ""
      return `- ${truncateText(primary, 180)}${description}`
    })
    return `${title}:\n${lines.join("\n")}`
  }

  function formatMcpToolsForRaven(serverName: string, tools: any[]): string {
    if (!tools.length) return `Tools for ${serverName}: none found.`
    const lines = tools.map((item) => {
      const description = item?.description ? `\n  Description: ${truncateText(item.description, 600)}` : ""
      const schema = item?.inputSchema ? `\n  Input schema: ${truncateText(JSON.stringify(item.inputSchema), 3000)}` : ""
      return `- ${item?.name ?? "(unnamed)"}${description}${schema}`
    })
    return `Tools for ${serverName}:\n${lines.join("\n")}`
  }

  function formatMcpResult(result: any): string {
    if (!result) return "No result."
    if (Array.isArray(result.content)) {
      const parts = result.content.map((part: any) => {
        if (part?.type === "text") return part.text
        return JSON.stringify(part)
      })
      return parts.join("\n") || JSON.stringify(result)
    }
    return typeof result === "string" ? result : JSON.stringify(result, null, 2)
  }

  function parseMcpArguments(raw: string | undefined): Record<string, any> {
    if (!raw?.trim()) return {}
    const parsed = JSON.parse(raw)
    if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) {
      throw new Error("argumentsJson must be a JSON object")
    }
    return parsed
  }

